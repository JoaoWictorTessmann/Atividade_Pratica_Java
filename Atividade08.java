public class Atividade08 {

    public static void main(String[] args) {
        int numero = 11;
        int numero2 = 11;

        if(numero > numero2) {
            System.out.println("O número " + numero + " é maior que " + numero2);
        } else if(numero < numero2) {
            System.out.println("O número " + numero + " é menor que " + numero2);
        } else {
            System.out.println("Os números são iguais.");
        }
    }
}