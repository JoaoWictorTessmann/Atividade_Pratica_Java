public class Atividade10 {

    public static void main(String[] args) {
        int numero = 0;

        if(numero > 0) {
            System.out.println("O número " + numero + " é positivo.");
        } else if(numero < 0) {
            System.out.println("O número " + numero + " é negativo.");
        } else {
            System.out.println("O número é zero.");
        }
    }
}